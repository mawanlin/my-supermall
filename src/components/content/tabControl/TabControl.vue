<template>
  <div class="tab-control">
    <div
      class="teb-control-item"
      v-for="(item, index) in titles"
      :key="item.id"
      @click="itemClick(index)"
      :class="{ active: currentIndex == index }"
    >
      <span>
        {{ item }}
      </span>
    </div>
  </div>
</template>

<script>
export default {
  name: "TabControl",
  components: {},
  props: {
    titles: {
      type: Array,
      default() {
        return [];
      },
    },
  },
  data() {
    return {
      currentIndex: 0,
    };
  },
  methods: {
    itemClick(index) {
      this.currentIndex = index;
      this.$emit("tabClick", index);
    },
  },
};
</script>

